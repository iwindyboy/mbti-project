// ═══════════ 외부 인터페이스 ═══════════

export interface AxisCoachingResult {
  growth: string[];
  relationship: string[];
  career: string[];
  selfDev: string[];
}

/**
 * 축별 분석 결과를 기반으로 맞춤 코칭 생성
 * 중복 문장 없이 각 영역에 고유한 코칭을 제공
 */
export function generateAxisCoaching(
  axisDetails: Array<{
    axis: string;
    isMatch: boolean;
    isGreyZone: boolean;
    isCoreAxis: boolean;
  }>
): AxisCoachingResult {
  const result: AxisCoachingResult = {
    growth: [],
    relationship: [],
    career: [],
    selfDev: []
  };

  for (const detail of axisDetails) {
    const axis = detail.axis as Axis;
    let coaching: AxisCoaching;

    if (detail.isGreyZone) {
      coaching = GREY_ZONE_COACHING[axis];
    } else if (detail.isMatch) {
      coaching = MATCH_COACHING[axis];
    } else {
      coaching = GAP_COACHING[axis];
    }

    // 핵심 축은 앞에, 일반 축은 뒤에 배치
    if (detail.isCoreAxis) {
      result.growth.unshift(coaching.growth);
      result.relationship.unshift(coaching.relationship);
      result.career.unshift(coaching.career);
      result.selfDev.unshift(coaching.selfDev);
    } else {
      result.growth.push(coaching.growth);
      result.relationship.push(coaching.relationship);
      result.career.push(coaching.career);
      result.selfDev.push(coaching.selfDev);
    }
  }

  // 각 영역 최대 3개로 제한 (핵심 축 우선)
  result.growth = result.growth.slice(0, 3);
  result.relationship = result.relationship.slice(0, 3);
  result.career = result.career.slice(0, 3);
  result.selfDev = result.selfDev.slice(0, 3);

  return result;
}
