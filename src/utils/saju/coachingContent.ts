export interface AxisCoachingResult {
  growth: string[];
  relationship: string[];
  career: string[];
  selfDev: string[];
  overallMessage?: string;    // v2 추가: 천간별 종합 코칭 메시지
}

/**
 * 축별 분석 결과를 기반으로 천간 맞춤 코칭 생성
 *
 * @param axisDetails  축별 분석 결과 (alignmentMapper 출력)
 * @param ilgan        일간 천간 문자 (예: '甲', '乙', ...) — v2 추가 파라미터
 *
 * v1 대비 변경:
 * - ilgan 파라미터 추가 → 천간별 맞춤 코칭 생성
 * - overallMessage 추가 → 천간별 종합 코칭 메시지
 * - 각 코칭 문장이 4단계 구조(공감→인사이트→강점·약점→실천)로 강화
 */
export function generateAxisCoaching(
  axisDetails: Array<{
    axis: string;
    isMatch: boolean;
    isGreyZone: boolean;
    isCoreAxis: boolean;
  }>,
  ilgan?: string   // v2: 선택적 파라미터 (없으면 기본 코칭 제공)
): AxisCoachingResult {
  const result: AxisCoachingResult = {
    growth: [],
    relationship: [],
    career: [],
    selfDev: []
  };

  // 천간 페르소나 가져오기 (없으면 기본 갑목)
  const persona = (ilgan && CHEONGAN_PERSONA[ilgan])
    ? CHEONGAN_PERSONA[ilgan]
    : CHEONGAN_PERSONA['甲'];

  let matchCount = 0;
  let gapCount = 0;
  let greyCount = 0;

  for (const detail of axisDetails) {
    const axis = detail.axis as Axis;
    const areas: CoachingArea[] = ['growth', 'relationship', 'career', 'selfDev'];

    for (const area of areas) {
      let text: string;

      if (detail.isGreyZone) {
        text = buildGreyZoneCoaching(persona, axis, area, detail.isCoreAxis);
      } else if (detail.isMatch) {
        text = buildMatchCoaching(persona, axis, area, detail.isCoreAxis);
      } else {
        text = buildGapCoaching(persona, axis, area, detail.isCoreAxis);
      }

      // 핵심 축은 앞에, 일반 축은 뒤에 배치
      if (detail.isCoreAxis) {
        result[area].unshift(text);
      } else {
        result[area].push(text);
      }
    }

    // 카운트
    if (detail.isGreyZone) greyCount++;
    else if (detail.isMatch) matchCount++;
    else gapCount++;
  }

  // 각 영역 최대 3개로 제한 (핵심 축 우선)
  result.growth = result.growth.slice(0, 3);
  result.relationship = result.relationship.slice(0, 3);
  result.career = result.career.slice(0, 3);
  result.selfDev = result.selfDev.slice(0, 3);

  // 종합 코칭 메시지
  result.overallMessage = buildOverallCoachingMessage(
    persona, matchCount, gapCount, greyCount
  );

  return result;
}


