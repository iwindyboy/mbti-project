function buildOverallCoachingMessage(
  persona: CheonganPersona,
  matchCount: number,
  gapCount: number,
  greyCount: number
): string {
  const total = matchCount + gapCount + greyCount;

  if (matchCount >= 4) {
    return `${persona.name}의 기운을 가진 당신은 타고난 기질을 놀라울 만큼 잘 살리고 있어요. ${persona.metaphor}처럼 ${persona.coreDrive} — 이 본능이 지금 삶에서 그대로 발현되고 있어요. 당신의 다음 과제는 이 강점을 "더 깊이" 파는 것이에요. 이미 방향은 맞으니, 속도를 조절하며 한 걸음 한 걸음 전문성을 쌓아가세요. ${persona.growthKey}`;
  }

  if (gapCount >= 3) {
    return `${persona.name}의 기운을 타고났지만, 삶에서 많은 변화와 적응을 해온 당신이에요. 이 차이는 "어긋남"이 아니라, 당신이 다양한 경험을 통해 성장한 증거예요. ${persona.metaphor}의 본래 힘과 후천적으로 키운 새로운 역량 — 이 두 가지를 모두 가진 사람은 드물어요. 핵심은 "어떤 상황에서 어떤 모드를 쓸 것인가"를 의식적으로 선택하는 거예요. ${persona.growthKey}`;
  }

  if (greyCount >= 3) {
    return `${persona.name}의 에너지를 가진 당신은 여러 영역에서 유연한 적응력을 보여요. 이건 "우유부단함"이 아니라, 다양한 환경에서 살아남은 생존 지능이에요. ${persona.metaphor}의 본래 기운과 결합하면, 당신은 어떤 환경에서든 자기만의 리듬을 찾을 수 있는 사람이에요. 다만, 유연함이 "정체성의 희석"이 되지 않도록 한 가지 핵심 가치를 정해보세요. ${persona.growthKey}`;
  }

  // 기본: 혼합형
  return `${persona.name}의 에너지를 가진 당신은 타고난 기질과 후천적 성장이 조화롭게 어우러져 있어요. 일부는 본래 모습 그대로, 일부는 새롭게 키운 면이 공존하고 있죠. 이 "복합성"이야말로 당신을 유니크하게 만드는 거예요. ${persona.metaphor}의 본래 힘 위에 경험이 만든 새로운 레이어가 얹혀져서, 더 입체적인 사람이 되고 있어요. ${persona.growthKey}`;
}


