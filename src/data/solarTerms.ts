/**
 * 특정 날짜가 몇 번째 절기 구간인지 판별
 * @returns 0=인월(입춘~경칩), 1=묘월(경칩~청명), ..., 11=축월(소한~입춘)
 */
export function getSajuMonthIndex(solarYear: number, solarMonth: number, solarDay: number, solarHour: number = 12): number {
  const terms = SOLAR_TERMS[solarYear];
  if (!terms) {
    // 데이터가 없는 연도는 근사값 사용
    console.warn(`절기 데이터 없음: ${solarYear}년. 근사값 사용.`);
    return getApproxMonthIndex(solarMonth, solarDay);
  }

  // 현재 시각을 분 단위 타임스탬프로 변환
  const currentTime = new Date(solarYear, solarMonth - 1, solarDay, solarHour).getTime();

  // 12절기 날짜를 Date로 변환
  const termDates: Date[] = terms.map((t, i) => {
    const [datePart, timePart] = t.split(' ');
    const [mm, dd] = datePart.split('-').map(Number);
    const [hh, min] = timePart.split(':').map(Number);
    
    // 소한(index 11)은 다음 해 1월
    // 실제로는 같은 해의 1월 (전년도 소한)
    // 주의: 입춘(index 0)이 2월이고, 소한(index 11)은 1월
    let termYear = solarYear;
    if (i === 11) {
      // 소한은 다음 해 1월일 수 있으므로, 배열의 값이 01-xx이면 같은 연도 데이터
      // SOLAR_TERMS 기준으로 이미 해당 연도의 데이터
    }
    
    return new Date(termYear, mm - 1, dd, hh, min);
  });

  // 입춘 이전이면 → 전년도 축월(소한 구간)
  if (currentTime < termDates[0].getTime()) {
    return 11; // 축월 (전년도 소한 ~ 올해 입춘)
  }

  // 어느 절기 구간에 속하는지 판별
  for (let i = 0; i < 11; i++) {
    if (currentTime >= termDates[i].getTime() && currentTime < termDates[i + 1].getTime()) {
      return i;
    }
  }

  // 대설 이후 ~ 다음 해 소한 이전 → 축월 아님, 해월(11번째 절기 이후)
  // 소한 이후면 축월
  if (currentTime >= termDates[11].getTime()) {
    return 11;
  }

  // 대설(index 10) 이후 ~ 소한(index 11) 이전
  return 10;
}

/**
 * 입춘 이전인지 판별 (연주 계산에 필요)
 */
export function isBeforeIpchun(solarYear: number, solarMonth: number, solarDay: number, solarHour: number = 0): boolean {
  const terms = SOLAR_TERMS[solarYear];
  if (!terms) return solarMonth < 2 || (solarMonth === 2 && solarDay < 4);
  
  const [datePart, timePart] = terms[0].split(' '); // 입춘
  const [mm, dd] = datePart.split('-').map(Number);
  const [hh, min] = timePart.split(':').map(Number);
  
  const ipchun = new Date(solarYear, mm - 1, dd, hh, min);
  const current = new Date(solarYear, solarMonth - 1, solarDay, solarHour);
  
  return current.getTime() < ipchun.getTime();
}

// 절기 데이터 없을 때 근사값
function getApproxMonthIndex(month: number, day: number): number {
  // 대략적 절기 날짜 기준
  const approxDates = [
    [2, 4], [3, 6], [4, 5], [5, 6], [6, 6], [7, 7],
    [8, 7], [9, 8], [10, 8], [11, 7], [12, 7], [1, 6]
  ];
  
  for (let i = 0; i < 11; i++) {
    const [m1, d1] = approxDates[i];
    const [m2, d2] = approxDates[i + 1];
    if ((month > m1 || (month === m1 && day >= d1)) &&
        (month < m2 || (month === m2 && day < d2))) {
      return i;
    }
  }
  return 11;
}
